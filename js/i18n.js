//  - 日本語の値には、子供向けに <ruby> でふりがなを入れている(他言語はプレーン)。
//  - 値は innerHTML として描画する前提(自前の信頼できる文字列のみ)。
//  - 取得は t(key): 現在の言語 → 英語 → 日本語 → key の順にフォールバック。
//    中国語・韓国語でまだ訳していない長い解説は、英語にフォールバックする。

